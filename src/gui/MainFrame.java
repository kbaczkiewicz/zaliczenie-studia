    private void addHeaderToComboBox(javax.swing.JComboBox<String> comboBox) {
        comboBox.removeAllItems();
        Set<String> keys = this.file.getHeader().keySet();
        Iterator<String> it = keys.iterator();
        while (it.hasNext()) {
            String key = it.next();
            comboBox.addItem(key);
        }
    }

    private void organizeSortButtons() {
        this.jRadioButton1.setSelected(true);
        ButtonGroup group = new ButtonGroup();
        group.add(this.jRadioButton1);
        group.add(this.jRadioButton2);
    }

    private List<String> getValuesFromTable(javax.swing.JTable table) {
        List<String> newContent = new ArrayList<>();
        for (int i = 0; i < this.file.getFieldsCount(); i++) {
            newContent.add(table.getModel().getValueAt(0, i).toString());
        }

        return newContent;
    }

    private javax.swing.table.DefaultTableModel createDefaultTableModel() {
        return new javax.swing.table.DefaultTableModel(
                new Object[][]{
                    this.file.getHeader().keySet().toArray(),},
                this.file.getHeader().keySet().toArray());
    }

