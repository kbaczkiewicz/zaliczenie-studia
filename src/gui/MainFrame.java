    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int field = this.file.getHeader().get(this.jComboBox2.getSelectedItem().toString());
        SortPatternInterface sorter = this.jRadioButton1.isSelected()
                ? new AscendingSortPattern()
                : new DescendingSortPattern();
        sorter.sort(this.file.getContents(), field);
        this.fileReader.resetCounter();
        this.jFrame2.setVisible(false);
        this.jFrame3.setVisible(true);
    }//GEN-LAST:event_jButton8ActionPerformed

