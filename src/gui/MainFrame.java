    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int returnValue = this.jFileChooser1.showOpenDialog(this);
        if (returnValue == this.jFileChooser1.APPROVE_OPTION) {
            File file = jFileChooser1.getSelectedFile();
            try {
                FileFactory factory = new FileFactory();
                this.file = factory.loadFile(file.getAbsolutePath());
                this.fileWriter = new file.Writer(this.file);
                this.jTextField1.setText(file.getAbsolutePath());
                this.createFrame();
                this.readNextLine();

            } catch (IOException ex) {
                this.jTextField1.setText("Nie udało się otworzyć pliku");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

